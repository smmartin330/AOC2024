def elapsed_time(start_time):
    return f"{round(time() - start_time, 8)}s\n"

class Monkey():
    def __init__(self,this_monkey):
        self.items = list(map(lambda item: int(item), this_monkey[1].split(': ')[1].split(', '))) # [79, 98]
        self.operation = this_monkey[2].strip()[17:] # 'old * 19'
        self.test = int(this_monkey[3].strip().split(' ')[3]) # 23
        self.if_true = int(this_monkey[4].strip().split(' ')[5]) # 2
        self.if_false = int(this_monkey[5].strip().split(' ')[5]) # 3
        self.counter = 0

class Puzzle():
    def __init__(self,input_text):
        self.input_text = input_text
        self.input_list = input_text.strip().split('\n\n')
        
    def init_monkeys(self):
        self.monkeys = []
        self.divisors = 1
        for monkey in self.input_list:
            self.monkeys.append(Monkey(this_monkey=monkey.split('\n')))
            self.divisors *= self.monkeys[-1].test
    
    def eval_items(self,monkey,round_factor):
        while len(monkey.items) > 0:
            monkey.counter += 1
            old = monkey.items.pop(0)
            new = math.floor(eval(monkey.operation)/round_factor)
            new = new - ( self.divisors * (new // self.divisors) )
            if new % monkey.test == 0:
                self.monkeys[monkey.if_true].items.append(new)
            else:
                self.monkeys[monkey.if_false].items.append(new)
                
    def do_round(self,round_factor):
        for monkey in self.monkeys:            
            self.eval_items(monkey,round_factor)
    
    def monkey_business(self):
        monkey_business = []
        for monkey in self.monkeys:
            monkey_business.append(monkey.counter)
        monkey_business.sort()
        return numpy.prod(monkey_business[-2:])
    
    def p1(self):
        self.init_monkeys()
        for _ in range(1,21):
            self.do_round(3)
            self.p1_solution = self.monkey_business()
        
        
    def p2(self):
        self.init_monkeys()
        for _ in range(1,10001):
            if _ % 1000 == 0:
                print(f"Round {_} - {self.monkey_business()}")
            self.do_round(1)
            self.p2_solution = self.monkey_business()

